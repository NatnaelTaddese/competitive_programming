import math


n = int(input())
a = list(map(int, input().split()))
# a.sort()

dp = [1] * (n + 1)


for i in range(n - 1, -1, -1):
    j = i + 1
    while j < n:
        if math.gcd(a[i], a[j]) > 1:
            dp[i] = max(dp[i], dp[j] + 1)
            break
        j += 1

print(max(dp))

